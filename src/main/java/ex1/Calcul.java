public class Calcul {

	/**
	 * Méthode prenant 2 entiers en arguments
	 *  et retournant la somme
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * Méthode prenant 2 entiers en arguments
	 *  et retournant la différence
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
